# .lower() — გარდაქმნის სტრინგის ყველა ასოს პატარა ასოდ
text = "PyTHon"
print(text.lower()) #"python"

# .upper() — გარდაქმნის სტრინგის ყველა ასოს დიდ ასოდ.

text = "hello"
print(text.upper()) #"HELLO"

# .capitalize() — სტრინგის პირველი ასო გახდება დიდი, ხოლო დანარჩენები პატარა.

text = "hELLo WOrLD"
print(text.capitalize()) #"Hello world"